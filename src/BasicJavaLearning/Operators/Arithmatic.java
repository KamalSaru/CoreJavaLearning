package BasicJavaLearning.Operators;

public class Arithmatic {
    public static void main(String[] args) {
        byte num1, num2; //Declaration the value
        num1 = 10;  //Initialization
        num2 = 5;   //Initialization

        System.out.println(num1 + num2);//num1 and numb2 are operand
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2); //0 it gives reminder value
        System.out.println(++num1); //11 pre increment
        System.out.println(--num1); //10 pre decrement
    }
}
